package com.example.smarttracker.data.hrm

import com.example.smarttracker.data.hrm.model.HrmConnectionState
import com.example.smarttracker.data.hrm.model.HrmSample
import com.example.smarttracker.data.hrm.model.HrmScanResult
import kotlinx.coroutines.flow.SharedFlow
import kotlinx.coroutines.flow.StateFlow

/**
 * Менеджер внешнего пульсометра — единая точка владения соединением.
 *
 * @Singleton: экран «Датчики» (скан/подключение без тренировки) и
 * LocationTrackingService (автоподключение на тренировке, пульс в GPS-точки)
 * инжектят ОДИН экземпляр — соединение переживает уход с экрана и живёт,
 * пока его явно не разорвут ([disconnect]) или не умрёт процесс.
 *
 * Политика переподключения — агрессивная, без лимита попыток: после
 * неожиданного обрыва менеджер ретраит подключение бесконечно с паузой ~1 с,
 * чтобы тренировка была максимально покрыта данными пульса. Ретраи
 * приостанавливаются только при выключенном Bluetooth-адаптере и
 * возобновляются при его включении.
 */
interface HrmManager {

    /** Состояние соединения — для бейджа на экране тренировки и экрана «Датчики». */
    val connectionState: StateFlow<HrmConnectionState>

    /**
     * Последний сэмпл пульса; null если данных нет или соединение разорвано
     * (при обрыве сбрасывается — устаревший пульс не показывается и не пишется).
     */
    val currentSample: StateFlow<HrmSample?>

    /**
     * Поток результатов сканирования. Эмитит найденные устройства пока скан
     * активен; одно устройство может приходить многократно (обновление RSSI) —
     * дедупликация по адресу на стороне подписчика.
     */
    val scanResults: SharedFlow<HrmScanResult>

    /** true пока идёт сканирование. */
    val isScanning: StateFlow<Boolean>

    /**
     * Начать сканирование пульсометров. Разрешения (BLUETOOTH_SCAN и т.д.)
     * должны быть выданы до вызова — проверяет UI.
     */
    fun startScan()

    /** Остановить сканирование. Безопасен для повторного вызова. */
    fun stopScan()

    /**
     * Подключиться к датчику по адресу и удерживать соединение
     * (бесконечный реконнект при обрывах). Останавливает активный скан.
     * Идемпотентен: повторный вызов с тем же адресом при живом соединении — no-op.
     */
    fun connect(address: String)

    /**
     * Разорвать соединение и прекратить реконнекты
     * (пользователь нажал «Забыть датчик» / сервис умирает).
     */
    fun disconnect()

    /**
     * Пульс для записи в GPS-точку: bpm последнего сэмпла, только если
     * соединение активно и сэмпл свежее [maxAgeMs]. Иначе null — точка
     * уходит без пульса, устаревшее значение после обрыва не размазывается
     * по треку.
     */
    fun freshBpm(maxAgeMs: Long = DEFAULT_FRESH_BPM_MAX_AGE_MS): Int?

    companion object {
        /** Дефолтное окно свежести сэмпла: датчики шлют ~1 сэмпл/с, 10 с — с запасом. */
        const val DEFAULT_FRESH_BPM_MAX_AGE_MS = 10_000L
    }
}
