package com.example.smarttracker.domain.model

/**
 * МОБ-6.3 — Domain модель для роли (доступной для выбора на регистрации).
 *
 * Отличается от Role.kt:
 * - Role: используется для ролей пользователя (после логина)
 * - RoleResponse: используется для списка доступных ролей (на экране регистрации)
 *
 * Содержит дополнительное поле description для отображения в UI (опциональное).
 * Обычно загружается один раз при старте приложения и кешируется.
 *
 * @param id Уникальный идентификатор роли
 * @param name Название роли для отображения ("Спортсмен", "Тренер" и т.д.)
 * @param description Описание роли для пользователя (может быть null если не отправлено с API)
 */
data class RoleResponse(
    val id: Int,
    val name: String,
    val description: String? = null
)
