package com.example.smarttracker.domain.repository

/**
 * Источник списка почтовых доменов, разрешённых для регистрации.
 *
 * Требование 149-ФЗ (поправки от 01.12.2023): регистрация пользователей
 * российских сервисов допускается только через российские почтовые сервисы,
 * номер телефона РФ, ЕСИА или российские ID-системы (VK ID, Yandex ID).
 *
 * Интерфейс в domain, чтобы источник списка можно было менять без правок
 * бизнес-логики: сейчас data-реализация возвращает захардкоженный список,
 * после появления эндпоинта на бэкенде — будет запрашивать его по сети
 * с кэшированием и fallback на локальный список.
 *
 * suspend — заложен заранее под будущую сетевую реализацию.
 */
interface AllowedEmailDomainsRepository {

    /**
     * Множество разрешённых доменов в нижнем регистре (например, "yandex.ru").
     * Не бросает исключений: реализация обязана вернуть непустой список
     * (при недоступности сети — локальный fallback).
     */
    suspend fun getAllowedDomains(): Set<String>
}
